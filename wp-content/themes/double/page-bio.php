            <h2 class="container__bio__header">Summer 1961:</h2>
            <div class="container__bio__text"> 
                <p>
                    Four young men from West Philadelphia come together for the first time as United Image.  “We were all just crazy about harmony,” Leonard “Butch” Davis explains. “We had a great deal of admiration for each other’s talents. And that made it easy for us to begin to want to work together.”  
                    Over the following years, the group performed at numerous talent shows, High School shows and House Parties in and around the Philadelphia area and occasionally would get a chance to be on a local show with recording artists.  “One of our biggest thrills,” Joe Harris explains, “came in 1963 when we were on a Saturday night talent show. The show just before ours starred Sam Cooke, Chuck Jackson, Dionne Warwick, Bobby Womack and The Crystals. It was our first taste of feeling what the big time was like.”
                </p> 
            </div>
            <h2 class="container__bio__header">1968</h2>
            <div class="container__bio__text">
                <p>
                    The group reunited after High School and military service. They begin their quest toward a lifelong dream of becoming successful entertainers and recording artists. A talented guitarist and old friend “Roland Chambers” hears they are getting back together and decides to help them out. Roland helps to polish the group’s harmony and gives them their first opportunity to get back on stage. The famed producers Kenny Gamble (soon to be of “Gamble & Huff” has a reunion show with his old band “Kenny Gamble & The Romeos”  The group, performing as United Image, gets to open for the band. 
                    “United Image” continues to gain exposure, working in local bars and clubs in and around the Philadelphia area and similar places from Boston to North Carolina. “We sometimes did as many as four shows a night and at times, we were not getting paid. It was hard, man,” Joe remembers.
                </p>
            </div>
            <h2 class="container__bio__header">1972:</h2>
            <div class="container__bio__text">
                <p>
                    Upon the recommendation of some local DJ’s, Untied Image is introduced to recording executive Lebaron Taylor who signs them to Stax Records. Having recently broken off from Atlantic Records, Stax Records is seeing incredible success as an independent label, with artists as prominent as Isaac Hayes, Johnnie Taylor, Rufus Thomas, and The Bar-Kays.
                    United Image releases “Love’s Creeping up on Me” on Stax Records.  The single received significant airplay in the Philadelphia, NY and DC areas. “We all had 9 to 5s,” said Joe, but one day my wife started arguing with me about why I was still with the group and that we weren’t going to get anywhere. All of a sudden “Love’s Creeping Up On Me” came on the radio. That ended the argument. It felt like a lifelong dream had come true to hear your record on the radio for the first time. Everybody was elated.”
                </p>
            </div>
            <h2 class="container__bio__header">1973:</h2>
            <div class="container__bio__text">
                <p>
                    After organizational issues begin at Stax Records, United Image moves to Branding Iron Records, and records “The African Bump.”  The record was a regional hit.  Stax Records collaborates briefly with Clive Davis and CBS Records, before becoming a part of R&B lore.
                    SalSoul Records is founded in NYC by the Cayre Brothers. Their model is focused on the emerging disco scene, and they become known for promoting the genre with the hottest artists for dance, with 12” long-playing singles to support the dance clubs.  The Cayre Brothers begin working with Allan W Felder and Norman Harris, Philadelphia-based songwriters who has already seen significant success with a cadre of artists locally and nationally. Allan Felder and Norman Harris wrote many hits together, from the 1960s (Honey and the Bees, the Delphonics) through the 1980s.  
                    Norman Harris, Ron Baker and Earl Young, all former members of Philadelphia Internationals MFSB band, create the publishing company, Baker-Harris-Young Productions. Songwriter Allan Felder and former MFSB keyboardist T.G. Conway join Baker-Harris-Young at Salsoul.
                </p>
            </div>
            <h2 class="container__bio__header">1975:</h2>
            <div class="container__bio__text">
                <p>
                    United Image is renamed Double Exposure.  The group comes to the attention of the Cayre Brothers through Allan Felder and Norman Harris. DE signs with Salsoul Records. 
                    Double Exposure’s first Salsoul release, Ten Percent is penned by T.G. Conway and Allan Felder.  It is recorded in the Sigma Sound Studios in Philadelphia.
                </p>
            </div>
            <h2 class="container__bio__header">1976:</h2>
            <div class="container__bio__text">
                <p>
                    “Ten Percent” is released in the spring of 1976, and is quickly followed by both the long-playing club version of the hit and the “Ten Percent” LP. Remixed by acclaimed DJ and producer Walter Gibbons, “Ten Percent – Disco Blend” is often referred to as the first 12-inch single made available for purchase to the US public.
                    The album features the title track goes all the way to No. 2 on the dance/disco charts and reaches No. 54 on the Billboard Hot 100, moving within the top 100 for several months.
                    “Ten Percent” “Every Man” and “My Love is Free” result in many DE bookings and performances across the country and overseas. 
                </p>
            </div>
            <h2 class="container__bio__header">1975:</h2>
            <div class="container__bio__text">
                <p>
                    Double Exposure’s first Salsoul release, Ten Percent is penned by T.G. Conway and Allan Felder.  It is recorded in the Sigma Sound Studios in center city Philadelphia.
                </p>
            </div>
            <h2 class="container__bio__header">1977:</h2>
            <div class="container__bio__text">
                <p>
                    Double Exposure is back in the Studio, recording its second album for SalSoul Records. Double Exposure appears on Soul Train performing <a href="#">“Ten Percent”</a> and <a href="#">“My Love is Free.”</a>
                 </p>
            </div>
            <div class="container__bio__parallax-1978"></div>
            <h2 class="container__bio__header">1978:</h2>
            <div class="container__bio__text">
                <p>
                    Salsoul releases “Fourplay” DE’s second album, demonstrating the range of talent of all of the members, and helping the group diversify from the dance club to the nightclub.  Hits include “Perfect Lover” and “Newsy Neighbors”. Double Exposure gets word that their hit “My Love Is Free” has gone gold in South Africa. They do a month long tour there.
                </p>
            </div>
            <h2 class="container__bio__header">1979:</h2>
            <div class="container__bio_text">
                <p>
                    Salsoul releases “Locker Room” the third and final originally-produced album by the original group members.  “I’ve Got the Hots (for Ya)” is released as the club favorite, while “Why Do We Have to Go Our Separate Ways” is the precursor to a generation of steamy ballads to come in R&B for many years.  
                    Double Exposure is booked to travel to Canada and Nairobi Kenya. While Disco as a genre is ending in the US, it continues to flourish elsewhere in the world. 
                </p>
            </div>
            <div class="container__bio__parallax-1982"></div>
            <h2 class="container__bio__header">1982:</h2>
            <div class="container__bio_text">
                <p>
                    Double Exposure records a free-standing single for Gold Coast Records.  The song “After All This Time” charts just below the Top 50 on the Dance Charts. 
                    Dance music is declining in the US. Overseas performances run at the same rate or higher than local performances and then slow down all together.  
                </p>
            </div>
            <h2 class="container__bio__header">1984:</h2>
            <div class="container__bio__text">
                <p>
                    Salsoul Records closes for business as the Cayre Brothers move on to promote their developing home video business.
                    1985 through the 1990s
                    The group disbands and follows their separate – some non-musical – ventures.  They remain close friends and stay in touch, always thinking that, one day, they would be performing together again.
                    Wallace “Jimmy” Williams becomes a lead singer for The Trammps, a well-known band also part of Baker, Harris and Young Productions.  
                </p>
            </div>
            <h2 class="container__bio__header">2001:</h2>
            <div class="container__bio__text">
                <p>
                    A dance group called M&S uses samples from Double Exposure's "Everyman" in their song called "Salsoul Nugget".
                </p>
            </div>
            <h2 class="container__bio__header">2007:</h2>
            <div class="container__bio__text">
                <p>
                    Double Exposure performance requests never cease.  Additional opportunities to perform together result in a new studio opportunity. The group records “Soul Recession” for Groove City record company, at Bobby Eli’s Groove Yard studios.  The recording is a rare limited and non-commercial release.  It is the last time all of the original members record together.
                </p>
            </div>
            <h2 class="container__bio__header">2010:</h2>
            <div class="container__bio__text">
